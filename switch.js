#!/usr/bin/node

const n = 40;

switch (n){
case 1:
    console.log(`${n} = 1`);
    break;
case 2:
    console.log(`${n} = 2`);
    break;
case 3:
    console.log(`${n} = 3`);
    break;
case 4:
    console.log(`${n} = 4`);
    break;
case 5:
    console.log(`${n} = 5`);
    break;
default:
    console.log("none of the above");
    break;
}
